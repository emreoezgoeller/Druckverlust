function sectionExpectations(values) {
  return [
    { label: 'Querschnittsfläche', path: 'area', expected: values.area, tolerance: 1e-10, unit: 'm²' },
    { label: 'Hydraulischer Durchmesser', path: 'hydraulicDiameter', expected: values.hydraulicDiameter, tolerance: 1e-10, unit: 'm' },
    { label: 'Luftgeschwindigkeit', path: 'velocity', expected: values.velocity, tolerance: 1e-9, unit: 'm/s' },
    { label: 'Dynamischer Druck', path: 'dynamicPressure', expected: values.dynamicPressure, tolerance: 1e-8, unit: 'Pa' },
    { label: 'Reibungsgefälle', path: 'frictionRate', expected: values.frictionRate, tolerance: 1e-9, unit: 'Pa/m' },
    { label: 'Reibungsverlust', path: 'frictionLoss', expected: values.frictionLoss, tolerance: 1e-8, unit: 'Pa' },
    { label: 'ζ-Verlust', path: 'zetaLoss', expected: values.zetaLoss, tolerance: 1e-8, unit: 'Pa' },
    { label: 'Gesamt ungerundet', path: 'totalLoss', expected: values.totalLoss, tolerance: 1e-8, unit: 'Pa' },
    { label: 'Gesamt auf 0,5 Pa aufgerundet', path: 'roundedTotalLoss', expected: values.roundedTotalLoss, tolerance: 1e-12, unit: 'Pa' },
  ];
}

